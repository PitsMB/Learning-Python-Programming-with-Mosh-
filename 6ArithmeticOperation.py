#Arithmetic operations

first_num = 10
second_num = 2

#Add
print(first_num + second_num)

#Subtract
print(first_num - second_num)

#Multiply
print(first_num * second_num)

#Divide
print(first_num / second_num)

#Modulo
print(first_num % second_num)

#raise to the power
print(first_num ** second_num)

#augmented assignment operator
first_num += second_num
print(first_num)