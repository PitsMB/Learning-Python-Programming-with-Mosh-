#String methods

word = 'Shit'

#return lenght of string
print(len(word))

#makes the string uppercase
print(word.upper())

#makes the string lowercase
print(word.lower())

#finds the index of a letter within a string
print(word.find('S'))

#replace the letter/word within a string
print(word.replace('i', '*'))

#return boolean value, checks if a letter/word is within a string
print('Sh' in word)